        pnlNegro.setBackground(Color.darkGray);
        pnlNegroResaltado.setBackground(new Color(120, pnlNegro.getBackground().getGreen() - 64, pnlNegro.getBackground().getBlue() - 64, 100));
        pnlBlanco.setBackground(Color.white);
        pnlBlancoResaltado.setBackground(new Color(120, pnlBlanco.getBackground().getGreen() - 64, pnlBlanco.getBackground().getBlue() - 64, 100));
