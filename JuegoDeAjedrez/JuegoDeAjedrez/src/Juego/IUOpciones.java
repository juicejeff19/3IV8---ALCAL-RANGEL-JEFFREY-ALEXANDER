    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (cmbBlancas.getSelectedIndex() == 0) {
            tablero.setTurnoComputadora(1);// TODO add your handling code here:
        } else if (cmbNegras.getSelectedIndex() == 0) {
            tablero.setTurnoComputadora(-1);// TODO add your handling code here:
        } else {
            tablero.setTurnoComputadora(0);
        }
        tablero.setSeleccionarAlternativas(chbxMostrarAlternetivas.isSelected());
        tablero.setNegro(pnlNegro.getBackground());
        tablero.setNegroResaltado(pnlNegroResaltado.getBackground());
        tablero.setBlanco(pnlBlanco.getBackground());
        tablero.setBlancoResaltado(pnlBlancoResaltado.getBackground());
        tablero.setSeleccionado(pnlSeleccionado.getBackground());
        tablero.setAlerta(pnlAlerta.getBackground());
        tablero.ordenarTablero();
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

